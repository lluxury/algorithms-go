/*

比较两个二叉树

- 递归

*/

func isSameTree(p *lib.TreeNode, q *lib.TreeNode) bool {
	if p == nil && q == nil {
		return true
	} else if q == nil || p == nil {
		return false
	}

	if p.Val != q.Val {
		return false
	}

	return isSameTree(p.Left, q.Left) && isSameTree(p.Right, q.Right)
}
